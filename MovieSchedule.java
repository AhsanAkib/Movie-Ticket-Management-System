public class MovieSchedule
{
	String movieName;
	String date;
	String month;
	String year;
	String time;
	public MovieSchedule()
	{}
	public MovieSchedule(String movieName, String date,String month, String year, String time)
	{
		this.movieName = movieName;
		this.date = date;
		this.month = month;
		this.year = year;
		this.time = time;
	}
	
} 